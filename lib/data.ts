export function enrichListing(listing: BaseListing, index: number): Listing {
  return {
    ...listing,
    maxGuests: getGuestCapacity(listing),
    allowsPets: index % 3 !== 1,
    availableDateKeys: AVAILABILITY_WINDOWS[index % AVAILABILITY_WINDOWS.length],
  };
}

function getGuestCapacity(listing: BaseListing) {
  if (listing.category === "team") return 8;
  if (listing.category === "coffee") return 6;
  if (listing.category === "loft") return 4;
  if (listing.category === "monitors" || listing.category === "ergonomic") return 4;
  return 2;
}

// Listing data (title/neighborhood) is stored in English. We append the Thai
// names of the places each listing mentions to its searchable text, so Thai
// queries — including partial ones typed character by character — match by
// plain substring just like English queries do.
const EN_TO_TH_LOCATIONS: Record<string, string> = {
  "ari samphan": "อารีย์สัมพันธ์",
  "ari": "อารีย์",
  "saphan khwai": "สะพานควาย",
  "chatuchak": "จตุจักร",
  "phahonyothin": "พหลโยธิน",
  "pradiphat": "ประดิพัทธ์",
  "kamphaeng phet": "กำแพงเพชร",
  "asok": "อโศก",
  "thong lo": "ทองหล่อ",
  "bangkok": "กรุงเทพ กรุงเทพมหานคร กรุงเทพฯ",
};

export function thaiLocationKeywords(text: string) {
  const lower = text.toLowerCase();
  const matched = Object.entries(EN_TO_TH_LOCATIONS)
    .filter(([english]) => lower.includes(english))
    .map(([, thai]) => thai);
  return matched.join(" ");
}

export function localSearch(
  listings: Listing[],
  query: string,
  category: string,
  bounds: { north: number; east: number; south: number; west: number },
  filters: Filters,
  sortBy: SortOption = "default",
  criteria?: SearchCriteria,
): Listing[] {
  const q = normalizeSearchQuery(query).toLowerCase().trim();
  const requiredGuests = criteria ? getRequiredGuestCapacity(criteria) : 0;
  let results = listings.filter((l) => {
    if (category !== "all" && l.category !== category) return false;
    if (l.lat > bounds.north || l.lat < bounds.south) return false;
    if (l.lng > bounds.east || l.lng < bounds.west) return false;
    if (q) {
      const haystack = `${l.title} ${l.category} ${l.neighborhood} ${thaiLocationKeywords(`${l.title} ${l.neighborhood}`)}`.toLowerCase();
      if (!haystack.includes(q)) return false;
    }
    if (l.pricePerNight < filters.priceMin || l.pricePerNight > filters.priceMax) return false;
    if (l.rating < filters.minRating) return false;
    if (requiredGuests > 0 && l.maxGuests < requiredGuests) return false;
    if (criteria?.guests.pets && !l.allowsPets) return false;
    if (criteria?.dateKey && !l.availableDateKeys.includes(criteria.dateKey)) return false;
    return true;
  });

  if (sortBy === "price_asc")  results = [...results].sort((a, b) => a.pricePerNight - b.pricePerNight);
  if (sortBy === "price_desc") results = [...results].sort((a, b) => b.pricePerNight - a.pricePerNight);
  if (sortBy === "rating")     results = [...results].sort((a, b) => b.rating - a.rating);

  return results;
}

export function normalizeSearchQuery(query: string) {
  const trimmedQuery = query.trim().toLowerCase();
  return trimmedQuery === "บริเวณใกล้เคียง" || trimmedQuery === "nearby" ? "" : query;
}

function getRequiredGuestCapacity(criteria: SearchCriteria) {
  return criteria.guests.adults + criteria.guests.children + criteria.guests.infants;
}
